window.applyLanguage = function() {
    const savedLang = localStorage.getItem('app_lang') || 'ar';
    const langData = window.translations[savedLang];
    
    if (!langData) return;

    // 1. ضبط اتجاه الصفحة
    document.documentElement.setAttribute('lang', savedLang);
    document.documentElement.setAttribute('dir', langData.dir || (savedLang === 'ar' ? 'rtl' : 'ltr'));
    
    // 2. ترجمة العناصر بذكاء 
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        const translation = langData[key];

        if (translation) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.setAttribute('placeholder', translation);
            } else if (element.tagName === 'ION-ICON') {
                element.setAttribute('title', translation);
            } else {
                const icon = element.querySelector('ion-icon, img');
                const textSpan = element.querySelector('span');

                if (textSpan) {
                    textSpan.innerHTML = translation;
                } else if (icon && element.textContent.trim() === "") {
                    element.setAttribute('title', translation);
                } else if (icon) {
                    element.innerHTML = icon.outerHTML + " " + translation;
                } else {
                    element.innerHTML = translation;
                }
            }
        }
    });

    // 3. تحديث أي أزرار اختيار لغة موجودة في الواجهة
    const langLabel = document.getElementById('current-lang-label');
    if(langLabel) langLabel.innerText = savedLang.toUpperCase();

    // 🚨 [ السحر هنا ] 🚨: ضبط القائمة المنسدلة لتقف على اللغة الحقيقية للتطبيق
    const langSelect = document.getElementById('language-select');
    if (langSelect) {
        langSelect.value = savedLang; 
    }
};

window.applyLanguage();